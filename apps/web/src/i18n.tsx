interface I18nContextValue {
  locale: Locale;
  setLocale(locale: Locale): void;
  t(key: TranslationKey, vars?: Record<string, string | number>): string;
  money(value: number): string;
  date(value: string): string;
  status(value: CommerceOrderStatus): string;
  productName(code: number, fallback: string): string;
  categoryName(value: string): string;
  provinceName(code: string, fallback: string): string;
}

const I18nContext = createContext<I18nContextValue | null>(null);

export function LanguageProvider({ children }: { children: ReactNode }) {
  const [locale, setLocaleState] = useState<Locale>(() => resolveInitialLocale());

  const setLocale = (next: Locale): void => {
    localStorage.setItem(STORAGE_KEY, next);
    setLocaleState(next);
  };

  useEffect(() => {
    document.documentElement.lang = locale;
  }, [locale]);

  const value = useMemo<I18nContextValue>(() => ({
    locale,
    setLocale,
    t: (key, vars) => translate(locale, key, vars),
    money: (amount) => new Intl.NumberFormat(locale === "es" ? "es-PA" : "en-US", { style: "currency", currency: "USD" }).format(amount),
    date: (value) => new Intl.DateTimeFormat(locale === "es" ? "es-PA" : "en-US", { dateStyle: "medium", timeStyle: "short" }).format(new Date(value)),
    status: (status) => translate(locale, `status.${status}` as TranslationKey),
    productName: (code, fallback) => productNames[code]?.[locale] ?? fallback,
    categoryName: (value) => categoryNames[value]?.[locale] ?? value,
    provinceName: (code, fallback) => provinceNames[code]?.[locale] ?? fallback,
  }), [locale]);

  return <I18nContext.Provider value={value}>{children}</I18nContext.Provider>;
}

export function useI18n(): I18nContextValue {
  const value = useContext(I18nContext);
  if (!value) throw new Error("useI18n must be used within LanguageProvider.");
  return value;
}

export function LanguageSelector({ className = "" }: { className?: string }) {
  const { locale, setLocale, t } = useI18n();
  return (
    <label className={`language-select ${className}`.trim()}>
      <span>{t("common.language")}</span>
      <select value={locale} onChange={(event) => setLocale(event.target.value as Locale)} aria-label={t("common.language")}>
        <option value="en">{t("common.english")}</option>
        <option value="es">{t("common.spanish")}</option>
      </select>
    </label>
  );
}

export function getCurrentLocale(): Locale {
  return resolveInitialLocale();
}

function translate(locale: Locale, key: TranslationKey, vars?: Record<string, string | number>): string {
  let text: string = translations[locale][key] ?? translations.en[key] ?? key;
  if (vars) {
    for (const [name, value] of Object.entries(vars)) {
      text = text.replaceAll(`{${name}}`, String(value));
    }
  }
  return text;
}

function resolveInitialLocale(): Locale {
  const saved = localStorage.getItem(STORAGE_KEY);
  if (saved === "en" || saved === "es") return saved;
  return navigator.language.toLowerCase().startsWith("es") ? "es" : "en";
}
